package day02__dataturleri;

import java.util.Locale;

public class C02_NonPrimitiveDataTurleri {

    public static void main(String[] args) {

        int sayi = 10;
        double ond = 3.5;

        String str = "Java candir" ;

        System.out.println(sayi); // 10

        System.out.println(str.toLowerCase()); // java candir

        System.out.println(str.toUpperCase()); // JAVA CANDIR

        /*
        1-primitive data turleri sadece değer saklayabilir
        Non primitive data türleri ise değer saklamanın yanında hazır method'lar da barındırır
        bu hazır methodlar sayesinde tanımlanmış işlemleri kolayca yapabiliriz
        2-Primitive data turlerinin küçük harf ile başlar
        Non primitive data türlerinin isimleri büyük harf ile başlar
         */
    }
}
